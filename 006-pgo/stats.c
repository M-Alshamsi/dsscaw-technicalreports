#define SPECIESCOUNT (sizeof(sdex) / sizeof(*sdex))

static void
print_species(const species* s){
  printf("%u %u %u %u %s", s->idx, s->atk, s->def, s->sta, tnames[s->t1]);
  if(s->t2 != TYPECOUNT){
    printf("+%s", tnames[s->t2]);
  }
  printf(" %s\n", s->name);
}

// print those entries containing type
static void
filter_by_type(pgo_types_e t){
  for(unsigned i = 0 ; i < SPECIESCOUNT ; ++i){
    if(sdex[i].t1 == t || sdex[i].t2 == t){
      print_species(&sdex[i]);
    }
  }
}

static void
print_cpms(void){
  printf("cpms:\n");
  for(int i = 1 ; i < 100 ; ++i){
    printf("%2d %f\n", i, cpm(i));
  }
}

// atk, def, and sta all ought be mod forms (i.e. sum of base and IV)
static int
calccp(unsigned atk, unsigned def, unsigned sta, unsigned halflevel){
  float cand = (atk * sqrt(def) * sqrt(sta) * pow(cpm(halflevel), 2)) / 10;
  return cand < 10 ? 10 : floor(cand);
}

// FIXME binary search on it
static unsigned
maxlevel_cp_bounded(unsigned atk, unsigned def, unsigned sta, int cpceil, int *cp){
  unsigned lastgood = 0;
  *cp = 0;
  for(unsigned hl = 1 ; hl <= MAX_HALFLEVEL ; ++hl){
    int tmpc = calccp(atk, def, sta, hl);
    if(tmpc <= cpceil){
      lastgood = hl;
      *cp = tmpc;
    }else{
      break;
    }
  }
  return lastgood;
}

static inline float
calc_eff_a(unsigned atk, unsigned halflevel){
  return cpm(halflevel) * atk;
}

static inline float
calc_eff_d(unsigned def, unsigned halflevel){
  return cpm(halflevel) * def;
}

static inline unsigned
calc_mhp(unsigned sta, unsigned halflevel){
  return floor(cpm(halflevel) * sta);
}

static inline float
calc_fit(float effa, float effd, unsigned mhp){
  return cbrt(effa * effd * mhp);
}

// returns integer part, sets *half to 1 if it's a +0.5
// (aren't guaranteed exact representation with floats)
static inline unsigned
halflevel_to_level(unsigned hl, unsigned* half){
  if(hl % 2 == 0){
    *half = 1;
  }else{
    *half = 0;
  }
  return (hl + 1) / 2;
}

typedef struct stats {
  unsigned atk, def, sta;   // base stats for the Form
  unsigned hlevel;          // halflevel 1..99
  unsigned ia, id, is;      // individual vector components
  float effa, effd;         // effective attack and defense
  unsigned mhp;             // max hit points
  unsigned cp;              // combat power
  float geommean;           // geometric mean of effa, effd, mhp
  struct stats* next;
} stats;

static int
update_optset(stats** osets, const species* s, unsigned ia, unsigned id, unsigned is, unsigned hl){
  stats **prev = osets;
  stats *cur;
  unsigned moda = s->atk + ia;
  unsigned modd = s->def + id;
  float effa = calc_eff_a(moda, hl);
  float effd = calc_eff_d(modd, hl);
  unsigned mods = s->sta + is;
  unsigned mhp = calc_mhp(s->sta + is, hl);
  while( (cur = *prev) ){
    if(hl < cur->hlevel){
      break; // we're a lower level than any on the list; insert
    }
    if(hl == cur->hlevel){ // need compare
      if(effa == cur->effa && effd == cur->effd && mhp == cur->mhp){
        // we're equal to something on the list; insert here
        break;
      }else if(effa <= cur->effa && effd <= cur->effd && mhp <= cur->mhp){ // we're worse, exit
        // we're strictly less than something on the list; exit
        return -1;
      }else if(effa >= cur->effa && effd >= cur->effd && mhp >= cur->mhp){
        // we're strictly better than something on the list; remove it and continue
        *prev = cur->next;
        free(cur);
      }else{
        // we're not comparable; continue
        prev = &cur->next;
      }
    }else{
      prev = &cur->next;
    }
  }
  if((cur = malloc(sizeof(*cur))) == NULL){
    return -1;
  }
  cur->atk = s->atk;
  cur->def = s->def;
  cur->sta = s->sta;
  cur->hlevel = hl;
  cur->ia = ia;
  cur->id = id;
  cur->is = is;
  cur->effa = effa;
  cur->effd = effd;
  cur->mhp = mhp;
  cur->cp = calccp(moda, modd, mods, cur->hlevel);
  cur->geommean = calc_fit(cur->effa, cur->effd, cur->mhp);
  cur->next = *prev;
  *prev = cur;
  return 0;
}

// print the optimal level/IV combinations bounded by the given cp
static int
print_cp_bounded(const species* s, int cpceil){
  stats* optsets = NULL;
  printf("%d-bounded set for %s:\n", cpceil, s->name);
  for(int iva = 0 ; iva < 16 ; ++iva){
    for(int ivd = 0 ; ivd < 16 ; ++ivd){
      for(int ivs = 0 ; ivs < 16 ; ++ivs){
        int cp;
        unsigned hl = maxlevel_cp_bounded(s->atk + iva, s->def + ivd, s->sta + ivs, cpceil, &cp);
        if(update_optset(&optsets, s, iva, ivd, ivs, hl) < 0){
          return -1;
        }
      }
    }
  }
  stats* collectopt = NULL;
  stats** qopt = &collectopt;
  float maxmean = -1;
  // print the optimal frontier (large), and extract the truly optimal sets (small)
  while(optsets){
    stats* cur;
    cur = optsets;
    optsets = cur->next;
    unsigned half;
    unsigned l = halflevel_to_level(cur->hlevel, &half);
    printf(" %u-%u-%u: %2u%s %4u %.3f %.3f %u %.3f\n",
      cur->ia, cur->id, cur->is, l, half ? ".5" : "",
      cur->cp, cur->effa, cur->effd, cur->mhp, cur->geommean);
    if(cur->geommean > maxmean){ // new optimal
      stats* c;
      // clean out existing true optimals
      while( (c = collectopt) ){
        collectopt = c->next;
        free(c);
      }
      collectopt = cur;
      qopt = &cur->next;
      cur->next = NULL;
      maxmean = cur->geommean;
    }else if(cur->geommean == maxmean){ // FIXME unsafe FP comparison
      *qopt = cur;
      qopt = &cur->next;
      cur->next = NULL;
    }else{
      free(cur);
    }
  }
  printf("TRULY OPTIMAL CONFIGURATIONS:\n");
  stats* tmp;
  while( (tmp = collectopt) ){
    unsigned half;
    unsigned l = halflevel_to_level(tmp->hlevel, &half);
    printf(" %u-%u-%u: %2u%s %4u %.3f %.3f %u %.3f\n",
      tmp->ia, tmp->id, tmp->is, l, half ? ".5" : "",
      tmp->cp, tmp->effa, tmp->effd, tmp->mhp, tmp->geommean);
    collectopt = tmp->next;
    free(tmp);
  }
  return 0;
}

static const species*
lookup_species(const char* name){
  for(unsigned i = 0 ; i < SPECIESCOUNT ; ++i){
    if(strcasecmp(sdex[i].name, name) == 0){
      return &sdex[i];
    }
  }
  return NULL;
}

int main(int argc, char **argv){
  if(argc > 1){
    while(*++argv){
      const species *s = lookup_species(*argv);
      if(s == NULL){
        fprintf(stderr, "no species named \"%s\", exiting\n", *argv);
        return EXIT_FAILURE;
      }
      print_cp_bounded(s, 1500);
    }
    return EXIT_SUCCESS;
  }
  for(int t = 0 ; t < TYPECOUNT ; ++t){
    printf("%s:\n", tnames[t]);
    filter_by_type(t);
  }
  print_cpms();
  return EXIT_SUCCESS;
}
